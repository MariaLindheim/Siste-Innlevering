
document.getElementById('startTekstHide1').addEventListener('click', gjemmOgVis);

function gjemmOgVis() {
    let x = document.getElementById('hideOgVis1');
    if (x.style.display == 'block') {
        x.style.display = 'none';
    } else {
        x.style.display = 'block';
    }
}

